function TagsRow(props) {
  return (
    <div
      className={clsx(
        ThemeClassNames.docs.docFooterTagsRow,
        'row margin-bottom--sm',
      )}>
      <div className="col">
        <TagsListInline {...props} />
      </div>
    </div>
  );
}

function GetHelp({ fill, className }) {
  const { siteConfig: { customFields } } = useDocusaurusContext();
  className = clsx("is-flex", className || styles.iconsContainer);
  return (
    <div className={className}>
      Get help: &nbsp;&nbsp;&nbsp;
      <a href={customFields.handles.github} target="_blank" rel="noopener noreferrer" aria-label="Discord Support">
        <div><GithubSvg fill={fill} /></div>
      </a>
      <a href={customFields.handles.discord} target="_blank" rel="noopener noreferrer" aria-label="Discord Support">
        <div><DiscordSvg fill={fill} /></div>
      </a>
    </div>
  )
}

function EditMetaRow({
  editUrl,
  lastUpdatedAt,
  lastUpdatedBy,
  formattedLastUpdatedAt,
}) {
  const { colorMode } = useColorMode();
  const isDark = colorMode == 'dark';
  const fill = isDark ? 'white' : 'black';
  return (
    <div className={clsx(ThemeClassNames.docs.docFooterEditMetaRow, 'row')}>
      <div className={clsx("col", styles.helpContainer)}>
        <GetHelp fill={fill} />
      </div>
      <div className={clsx('col', styles.lastUpdated)}>
        {(lastUpdatedAt || lastUpdatedBy) && (
          <LastUpdated
            lastUpdatedAt={lastUpdatedAt}
            formattedLastUpdatedAt={formattedLastUpdatedAt}
            lastUpdatedBy={lastUpdatedBy}
          />
        )}
        {editUrl && <div><EditThisPage editUrl={editUrl} /></div>}
      </div>
    </div>
  );
}

export default function DocItemFooter(props) {
  const { content: DocContent } = props;
  const { metadata } = DocContent;
  const { editUrl, lastUpdatedAt, formattedLastUpdatedAt, lastUpdatedBy, tags } =
    metadata;
  const canDisplayTagsRow = tags.length > 0;
  const canDisplayEditMetaRow = !!(editUrl || lastUpdatedAt || lastUpdatedBy);
  const canDisplayFooter = canDisplayTagsRow || canDisplayEditMetaRow;

  if (!canDisplayFooter) {
    return null;
  }

  return (
    <footer
      className={clsx(ThemeClassNames.docs.docFooter, 'docusaurus-mt-lg')}>
      {canDisplayTagsRow && <TagsRow tags={tags} />}
      {canDisplayEditMetaRow && (
        <EditMetaRow
          editUrl={editUrl}
          lastUpdatedAt={lastUpdatedAt}
          lastUpdatedBy={lastUpdatedBy}
          formattedLastUpdatedAt={formattedLastUpdatedAt}
        />
      )}
    </footer>
  );
}

